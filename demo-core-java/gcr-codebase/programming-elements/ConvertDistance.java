// a program to convert the distance of 10.8 kilometers to miles.
public class ConvertDistance {
    public static void main(String[] args) {
        double distanceKm = 10.8;

        // it is to converting kilometers to miles
        double distanceMiles = distanceKm * 0.621371;
        System.out.println("The distance "+distanceKm+" kilometers in miles is "+distanceMiles);
    }
}
